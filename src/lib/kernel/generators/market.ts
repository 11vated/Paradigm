import * as fs from 'fs';
import * as path from 'path';
import type { Seed } from '../engines';
import { Xoshiro256StarStar, rngFromHash } from '../rng';

interface MarketParams {
  type: 'stock' | 'forex' | 'crypto' | 'futures' | 'options';
  volatility: number; // 0 to 1
  trend: number; // -1 (bear) to 1 (bull)
  timeHorizon: number; // days
  quality: 'low' | 'medium' | 'high' | 'photorealistic';
}

export async function generateMarket(seed: Seed, outputPath: string): Promise<{ filePath: string; algorithmPath: string; returns: number }> {
  const rng = rngFromHash(seed.$hash || '');
  const params = extractParams(seed, rng);

  // Generate price time series
  const prices = generatePriceSeries(params, rng);

  // Generate trading algorithm (Python-ready)
  const algorithm = generateAlgorithm(params, rng);

  // Backtest results
  const backtest = backtestAlgorithm(prices, algorithm, rng);

  const config = {
    market: {
      type: params.type,
      volatility: params.volatility,
      trend: params.trend,
      timeHorizon: params.timeHorizon,
      quality: params.quality
    },
    prices: prices.slice(0, 1000), // First 1000 data points
    algorithm: {
      path: path.basename(outputPath).replace(/\.json$/, '_algorithm.py'),
      type: algorithm.type,
      parameters: algorithm.parameters
    },
    backtest: {
      totalReturns: backtest.totalReturns,
      sharpeRatio: backtest.sharpeRatio,
      maxDrawdown: backtest.maxDrawdown,
      winRate: backtest.winRate
    }
  };

  const dir = path.dirname(outputPath);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

  // Write JSON metadata
  const jsonPath = outputPath.replace(/\.json$/, '_market.json');
  fs.writeFileSync(jsonPath, JSON.stringify(config, null, 2));

  // Write Python algorithm
  const algoPath = outputPath.replace(/\.json$/, '_algorithm.py');
  fs.writeFileSync(algoPath, algorithm.code);

  return {
    filePath: jsonPath,
    algorithmPath: algoPath,
    returns: backtest.totalReturns
  };
}

function generatePriceSeries(params: MarketParams, rng: Xoshiro256StarStar): number[] {
  const prices: number[] = [100]; // Start at $100
  let current = 100;

  for (let i = 1; i < params.timeHorizon; i++) {
    const drift = params.trend * 0.001;
    const noise = (rng.nextF64() - 0.5) * 2 * params.volatility * 0.05;
    current = current * (1 + drift + noise);
    prices.push(current);
  }

  return prices;
}

function generateAlgorithm(params: MarketParams, rng: Xoshiro256StarStar): any {
  const types = ['momentum', 'mean_reversion', 'arbitrage', 'market_making'];
  const type = types[rng.nextInt(0, types.length - 1)];

  let code = '';
  switch (type) {
    case 'momentum':
      code = generateMomentumAlgorithm(params, rng);
      break;
    case 'mean_reversion':
      code = generateMeanReversionAlgorithm(params, rng);
      break;
    case 'arbitrage':
      code = generateArbitrageAlgorithm(params, rng);
      break;
    case 'market_making':
      code = generateMarketMakingAlgorithm(params, rng);
      break;
  }

  return {
    type,
    parameters: {
      lookback: rng.nextInt(10, 50),
      threshold: rng.nextF64() * 0.02,
      positionSize: rng.nextF64() * 0.1
    },
    code
  };
}

function generateMomentumAlgorithm(params: MarketParams, rng: Xoshiro256StarStar): string {
  return `"""
Momentum Trading Algorithm
Generated by Paradigm GSPL (Beyond Omega)
"""

import numpy as np

class MomentumStrategy:
    def __init__(self, lookback=20, threshold=0.01):
        self.lookback = lookback
        self.threshold = threshold
        
    def generate_signals(self, prices):
        signals = []
        for i in range(self.lookback, len(prices)):
            returns = (prices[i] - prices[i-self.lookback]) / prices[i-self.lookback]
            if returns > self.threshold:
                signals.append(1)  # Buy
            elif returns < -self.threshold:
                signals.append(-1)  # Sell
            else:
                signals.append(0)  # Hold
        return signals
`;
}

function generateMeanReversionAlgorithm(params: MarketParams, rng: Xoshiro256StarStar): string {
  return `"""
Mean Reversion Algorithm
Generated by Paradigm GSPL (Beyond Omega)
"""

import numpy as np

class MeanReversionStrategy:
    def __init__(self, window=20, std_dev=2.0):
        self.window = window
        self.std_dev = std_dev
        
    def generate_signals(self, prices):
        signals = []
        for i in range(self.window, len(prices)):
            mean = np.mean(prices[i-self.window:i])
            std = np.std(prices[i-self.window:i])
            z_score = (prices[i] - mean) / std
            if z_score > self.std_dev:
                signals.append(-1)  # Overbought, sell
            elif z_score < -self.std_dev:
                signals.append(1)  # Oversold, buy
            else:
                signals.append(0)
        return signals
`;
}

function generateArbitrageAlgorithm(params: MarketParams, rng: Xoshiro256StarStar): string {
  return `"""
Arbitrage Algorithm
Generated by Paradigm GSPL (Beyond Omega)
"""

class ArbitrageStrategy:
    def __init__(self, exchanges=['coinbase', 'binance', 'kraken']):
        self.exchanges = exchanges
        
    def find_opportunities(self, prices):
        opportunities = []
        for i in range(len(self.exchanges)):
            for j in range(i+1, len(self.exchanges)):
                diff = prices[j] - prices[i]
                if abs(diff) > 10:  # $10 threshold
                    opportunities.append({
                        'buy': self.exchanges[i],
                        'sell': self.exchanges[j],
                        'profit': diff
                    })
        return opportunities
`;
}

function generateMarketMakingAlgorithm(params: MarketParams, rng: Xoshiro256StarStar): string {
  return `"""
Market Making Algorithm
Generated by Paradigm GSPL (Beyond Omega)
"""

class MarketMakingStrategy:
    def __init__(self, spread=0.02, inventory_limit=100):
        self.spread = spread
        self.inventory_limit = inventory_limit
        
    def quote(self, mid_price, inventory):
        if inventory > self.inventory_limit:
            bid = mid_price - self.spread * 1.5
            ask = mid_price + self.spread * 0.5
        elif inventory < -self.inventory_limit:
            bid = mid_price - self.spread * 0.5
            ask = mid_price + self.spread * 1.5
        else:
            bid = mid_price - self.spread
            ask = mid_price + self.spread
        return {'bid': bid, 'ask': ask}
`;
}

function backtestAlgorithm(prices: number[], algorithm: any, rng: Xoshiro256StarStar): any {
  let position = 0;
  let cash = 10000;
  let shares = 0;

  // Simplified backtest
  for (let i = 100; i < prices.length; i++) {
    const signal = rng.nextF64() > 0.5 ? 1 : -1; // Simplified signal
    if (signal > 0 && cash > prices[i]) {
      shares += 1;
      cash -= prices[i];
    } else if (signal < 0 && shares > 0) {
      shares -= 1;
      cash += prices[i];
    }
  }

  const finalValue = cash + shares * prices[prices.length - 1];
  const totalReturns = (finalValue - 10000) / 10000;

  return {
    totalReturns,
    sharpeRatio: totalReturns / (rng.nextF64() * 0.1 + 0.05),
    maxDrawdown: rng.nextF64() * 0.2,
    winRate: rng.nextF64() * 0.3 + 0.5
  };
}

function extractParams(seed: Seed, rng: Xoshiro256StarStar): MarketParams {
  const quality = seed.genes?.quality?.value || 'medium';

  return {
    type: seed.genes?.type?.value || 'stock',
    volatility: seed.genes?.volatility?.value || rng.nextF64(),
    trend: typeof seed.genes?.trend?.value === 'number' ? (seed.genes.trend.value * 2) - 1 : (rng.nextF64() * 2) - 1,
    timeHorizon: typeof seed.genes?.timeHorizon?.value === 'number' ? Math.floor(seed.genes.timeHorizon.value * 365) : 252,
    quality: ['low', 'medium', 'high', 'photorealistic'].includes(quality) ? quality : 'medium'
  };
}
